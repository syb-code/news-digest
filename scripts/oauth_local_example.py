# Use Google's OAuth 2.0 Playground to obtain a Refresh Token for Gmail API:
# https://developers.google.com/oauthplayground
# 1) Select scope: https://mail.google.com/
# 2) Authorize APIs (with your collector Gmail)
# 3) Exchange authorization code for tokens
# 4) Copy the Refresh Token to GitHub → Settings → Secrets as GMAIL_REFRESH_TOKEN
